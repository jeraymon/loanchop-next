<h3>Calculation Instructions:</h3>
<ol>
<li>Enter loan details by pressing the EDIT LOAN button. Enter the loan amount, interest rate, term and first payment date</li>
<li>Enter extra payment information. In the amortization table enter prepayment information. Repeating extra payments are applied to all payments after the entry. Single extra payments are only applied to the selected month.</li>
<li>Pressing the UPDATE button. Loan modifications will be incorporated into the calculations.</li>
</ol>
