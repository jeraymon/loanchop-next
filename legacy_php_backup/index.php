<h3>Description:</h3>
<div class="ajtext">
Reduce the amount of interest paid and pay your loan off early with extra payments. Don't settle for making payments for the full term your loan. This calculator determines the amount of money and time saved from prepayments on a mortgage or loan. It assumes the extra payments are applied to principal.  Extra payments are directly entered in the amortization table. In addition, it includes charts and graphs to help visually depict the savings. This calculator also has the ability to save any entered data via local storage. The data is stored directly on your device.
<br><br>
Note: This calculator can only be used on fixed interest or installment loans. Fixed interest loans have a set payment which does not fluctuate. It cannot be used for variable or revolving debt, credit card debt or adjustable rate loans such as ARMs.
<br><br>
Tips:
<ol>
<li>Specify extra payments should be applied to principal. Some banks and lenders may apply prepayments to the next payment or to an escrow account. The escrow account includes funds for taxes and insurance.</li>
<li>Making extra payments early in the loan term has the greatest impact. This is due to the fact, payments at the beginning of the loan goes mostly towards interest.</li>
<li>If your are planning to make extra payments for the life of the loan, you might consider refinancing your loan with a shorter term. Shorter term loans typically have a lower interest rate. However you lose the option of reducing your payments if the need arises by suspending your prepayments.</li>
<li>When making extra payments make sure there is not a penalty for prepayment. This information should be included in your loan documentation. Also, your loan servicer, lawyer or attorney should be able to help or provide you with this information.</li>
<li>Pay your loan on time. Late fees and penalties add up and reduce the amount of principal reduction.</li>
</ol>
This calculator can be used for homes, real estate, land, cars, student loans, trucks, motorcycles, boats, RVs and commercial business investments.
</div>
